/*Write a program in JAVA to check the upper and lower limits of primitive data types.*/

public class Demo {
   public static void main(String[] args) {
      System.out.println("Limits of primitive DataTypes");
      System.out.println("Byte Datatype values...");
      System.out.println("The Min of byte data type = " + Byte.MIN_VALUE);
      System.out.println("The Max of byte data type = " + Byte.MAX_VALUE);
      System.out.println("Short Datatype values...");
      System.out.println("The Min of short data type= " + Short.MIN_VALUE);
      System.out.println("The Max of short data type = " + Short.MAX_VALUE);
      System.out.println("Integer Datatype values...");
      System.out.println("The Min of int data type = " + Integer.MIN_VALUE);
      System.out.println("The Max of int data type = " + Integer.MAX_VALUE);
      System.out.println("Float Datatype values...");
      System.out.println("The Min of float data type = " + Float.MIN_VALUE);
      System.out.println("The Max of float data type = " + Float.MAX_VALUE);
      System.out.println("Double Datatype values...");
      System.out.println("The Min of double data type = " + Double.MIN_VALUE);
      System.out.println("The Max of double data type = " + Double.MAX_VALUE);
   }
}